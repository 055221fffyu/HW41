const sqlite3 = require('sqlite3').verbose();
const fs = require('fs');
const path = require('path');

const dbDir = path.join(__dirname, 'db');
const dbPath = path.join(dbDir, 'sqlite.db');

// 確保 db 目錄存在
if (!fs.existsSync(dbDir)) {
  fs.mkdirSync(dbDir);
}

// 開啟資料庫
const db = new sqlite3.Database(dbPath, (err) => {
  if (err) {
    console.error('無法開啟資料庫:', err.message);
  } else {
    console.log('成功開啟資料庫');
    initTable();
  }
});

// 建立 table（若不存在）
function initTable() {
  const createTableSQL = `CREATE TABLE IF NOT EXISTS alchol_prices (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    date TEXT NOT NULL,
    price_14 TEXT NOT NULL,
    price_133 TEXT NOT NULL,
    price_134 TEXT NOT NULL,
    price_135 TEXT NOT NULL,
    price_136 TEXT NOT NULL
  )`;
  db.run(createTableSQL, (err) => {
    if (err) {
      console.error('建立 table 失敗:', err.message);
    } else {
      console.log('alchol_prices table 已確認存在');
      checkAndInsertData();
    }
  });
}

// 僅在 table 為空時匯入資料
function checkAndInsertData() {
  db.get('SELECT COUNT(*) as count FROM alchol_prices', (err, row) => {
    if (err) {
      console.error('查詢 table 失敗:', err.message);
      return;
    }
    if (row.count === 0) {
      insertAlcoholData();
    } else {
      console.log('資料已存在，略過匯入');
    }
  });
}

